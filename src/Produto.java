class Produto {
    String nome;
    String descricao;
    double precoVenda;
    int quantidade;
    
    public Produto(String nome, String descricao, double precoVenda, int quantidade) {
        this.nome = nome;
        this.descricao = descricao;
        this.precoVenda = precoVenda;
        this.quantidade = quantidade;
    }
}